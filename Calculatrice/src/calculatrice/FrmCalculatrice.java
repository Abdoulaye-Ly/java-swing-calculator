    private void btnEgalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEgalActionPerformed
        // TODO add your handling code here:
        double resultat = 0;
        String operation = txtEcran.getText();
            if(operation.contains("+")){
                 String[] ops = operation.replace("+", "pp").split("pp");
                resultat = Double.valueOf(ops[0]) + Double.valueOf(ops[1]);
            }
            
            if(operation.contains("-")){
                 String[] ops = operation.replace("-", "mm").split("mm");
                resultat = Double.valueOf(ops[0]) - Double.valueOf(ops[1]);
            }
            
            if(operation.contains("*")){
                 String[] ops = operation.replace("*", "ff").split("ff");
                resultat = Double.valueOf(ops[0]) * Double.valueOf(ops[1]);
            }
            
            if(operation.contains("/")){
                String[] ops = operation.replace("/", "dd").split("dd");
                if("0".equals(ops[1])) {
                    txtEcran.setText(operation + "=Erreur");
                } else {
                    resultat = Double.valueOf(ops[0]) / Double.valueOf(ops[1]);
                }
            }
        txtEcran.setText(operation +"="+resultat);
           
    }//GEN-LAST:event_btnEgalActionPerformed

